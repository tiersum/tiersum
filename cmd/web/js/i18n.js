let currentLocale = (() => {
    try {
        const stored = localStorage.getItem(STORAGE_KEY);
        if (stored && messages[stored]) return stored;
    } catch { /* ignore */ }
    const browser = (navigator.language || navigator.userLanguage || '').slice(0, 2);
    return messages[browser] ? browser : DEFAULT_LOCALE;
})();

function t(key, vars = {}) {
    const locale = currentLocale;
    const dict = messages[locale] || messages[DEFAULT_LOCALE];
    let text = dict[key];
    if (text === undefined) {
        text = messages[DEFAULT_LOCALE][key] || key;
    }
    return text.replace(/\{(\w+)\}/g, (_m, k) => (vars[k] !== undefined ? String(vars[k]) : _m));
}

function setLocale(locale) {
    if (!messages[locale]) return;
    currentLocale = locale;
    try {
        localStorage.setItem(STORAGE_KEY, locale);
    } catch { /* ignore */ }
    // Update html lang attribute
    if (typeof document !== 'undefined') {
        document.documentElement.lang = locale === 'zh' ? 'zh-CN' : 'en';
    }
}

function getLocale() {
    return currentLocale;
}

function getAvailableLocales() {
    return Object.keys(messages);
}

export { t, setLocale, getLocale, getAvailableLocales };
